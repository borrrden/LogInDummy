#ifdef _MSC_VER
#include <windows.h>
#endif
#include <GL/GL.h>
#include <XPLMGraphics.h>

int bitmap_to_gl(unsigned char* data, int width, int height, GLenum format)
{
    int texture;
    XPLMGenerateTextureNumbers(&texture, 1);
    XPLMBindTexture2d(texture, 0);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB8, width, height, 0, format, GL_UNSIGNED_BYTE, data);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

    return texture;
}
